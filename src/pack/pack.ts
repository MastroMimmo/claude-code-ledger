import * as fs from 'node:fs';
import * as path from 'node:path';
import type { Store } from '../store/store';
import type { LedgerEvent, Session } from '../store/types';

export interface ContextPack {
  version: 1;
  title: string;
  createdAt: string;
  environment: {
    cwd: string | null;
    gitBranch: string | null;
    gitCommit: string | null;
  };
  sessions: Session[];
  summary: {
    events: number;
    byType: Record<string, number>;
    redactions: Record<string, number>;
  };
  prompts: string[];
  filesTouched: { reads: string[]; edits: string[] };
  commands: string[];
  tests: string[];
  openThreads: string[];
  notes: string[];
  /** Full redacted events, enabling lossless re-import. */
  events: LedgerEvent[];
}

export interface ImportResult {
  sessions: number;
  events: number;
}

const TODO_RE = /\b(TODO|FIXME|HACK|XXX|next step|follow[- ]?up|open question)\b/i;

function defaultTitle(sessions: Session[]): string {
  if (sessions.length === 1) return `Context pack - session ${sessions[0]!.id}`;
  return `Context pack - ${sessions.length} sessions`;
}

/** Aggregate the events of the given sessions into a transferable pack. */
export function buildPack(
  store: Store,
  sessionIds: string[],
  opts: { title?: string } = {},
  now: string = new Date().toISOString(),
): ContextPack {
  const sessions: Session[] = [];
  const events: LedgerEvent[] = [];
  for (const id of sessionIds) {
    const s = store.getSession(id);
    if (!s) continue;
    sessions.push(s);
    events.push(...store.getEvents(id));
  }

  const byType: Record<string, number> = {};
  const redactions: Record<string, number> = {};
  const prompts: string[] = [];
  const reads = new Set<string>();
  const edits = new Set<string>();
  const commands = new Set<string>();
  const tests = new Set<string>();
  const openThreads: string[] = [];
  const notes: string[] = [];

  for (const e of events) {
    byType[e.type] = (byType[e.type] ?? 0) + 1;
    if (e.redactions) {
      for (const [k, n] of Object.entries(e.redactions)) redactions[k] = (redactions[k] ?? 0) + n;
    }
    const summary = e.summary ?? '';
    switch (e.type) {
      case 'prompt':
        if (summary) prompts.push(summary);
        if (TODO_RE.test(summary)) openThreads.push(summary);
        break;
      case 'file_read':
        if (summary) reads.add(summary);
        break;
      case 'file_edit':
        if (summary) edits.add(summary);
        break;
      case 'bash':
        if (summary) commands.add(summary);
        break;
      case 'test':
        if (summary) tests.add(summary);
        break;
      case 'note':
        if (summary) notes.push(summary);
        if (TODO_RE.test(summary)) openThreads.push(summary);
        break;
      default:
        break;
    }
  }

  const last = sessions[sessions.length - 1];
  return {
    version: 1,
    title: opts.title ?? defaultTitle(sessions),
    createdAt: now,
    environment: {
      cwd: last?.cwd ?? null,
      gitBranch: last?.gitBranch ?? null,
      gitCommit: last?.gitCommit ?? null,
    },
    sessions,
    summary: { events: events.length, byType, redactions },
    prompts,
    filesTouched: { reads: [...reads], edits: [...edits] },
    commands: [...commands],
    tests: [...tests],
    openThreads,
    notes,
    events,
  };
}

/** Render a human-readable Markdown handoff from a pack. */
export function renderMarkdown(pack: ContextPack): string {
  const lines: string[] = [];
  const add = (s = '') => lines.push(s);

  add(`# ${pack.title}`);
  add();
  add(`> Generated by Claude Code Ledger on ${pack.createdAt}.`);
  add();

  add('## Environment');
  add(`- cwd: \`${pack.environment.cwd ?? '-'}\``);
  add(`- branch: \`${pack.environment.gitBranch ?? '-'}\`  ·  commit: \`${pack.environment.gitCommit ?? '-'}\``);
  add(`- sessions: ${pack.sessions.length}  ·  events: ${pack.summary.events}`);
  add();

  if (pack.prompts.length) {
    add('## Intents / prompts');
    for (const p of pack.prompts) add(`- ${p}`);
    add();
  }

  if (pack.filesTouched.edits.length || pack.filesTouched.reads.length) {
    add('## Files touched');
    if (pack.filesTouched.edits.length) {
      add('Edited:');
      for (const f of pack.filesTouched.edits) add(`- \`${f}\``);
    }
    if (pack.filesTouched.reads.length) {
      add('Read:');
      for (const f of pack.filesTouched.reads) add(`- \`${f}\``);
    }
    add();
  }

  if (pack.commands.length) {
    add('## Commands');
    for (const c of pack.commands) add(`- \`${c}\``);
    add();
  }

  if (pack.tests.length) {
    add('## Tests');
    for (const t of pack.tests) add(`- \`${t}\``);
    add();
  }

  if (pack.notes.length) {
    add('## Notes / decisions');
    for (const n of pack.notes) add(`- ${n}`);
    add();
  }

  if (pack.openThreads.length) {
    add('## Open threads');
    for (const o of pack.openThreads) add(`- ${o}`);
    add();
  }

  add('## Redactions');
  const reds = Object.entries(pack.summary.redactions);
  if (reds.length) {
    for (const [k, n] of reds) add(`- ${k}: ${n}`);
  } else {
    add('- none');
  }
  add();

  return lines.join('\n');
}

function slugify(s: string): string {
  return s.replace(/[^a-zA-Z0-9_-]+/g, '-').slice(0, 40) || 'pack';
}

/** Write `<base>.json` and `<base>.md` into `outDir`. */
export function writePack(pack: ContextPack, outDir: string): { jsonPath: string; mdPath: string } {
  fs.mkdirSync(outDir, { recursive: true });
  const slug = slugify(pack.sessions[0]?.id ?? 'pack');
  const stamp = pack.createdAt.replace(/[:.]/g, '-');
  const base = `pack-${slug}-${stamp}`;
  const jsonPath = path.join(outDir, `${base}.json`);
  const mdPath = path.join(outDir, `${base}.md`);
  fs.writeFileSync(jsonPath, JSON.stringify(pack, null, 2) + '\n');
  fs.writeFileSync(mdPath, renderMarkdown(pack));
  return { jsonPath, mdPath };
}

/** Re-import a pack's sessions and events into a store. */
export function importPack(store: Store, jsonPath: string): ImportResult {
  const pack = JSON.parse(fs.readFileSync(jsonPath, 'utf8')) as ContextPack;
  if (pack.version !== 1) throw new Error(`Unsupported pack version: ${String(pack.version)}`);

  let sessions = 0;
  for (const s of pack.sessions) {
    store.upsertSession({
      id: s.id,
      cwd: s.cwd,
      startedAt: s.startedAt,
      gitBranch: s.gitBranch,
      gitCommit: s.gitCommit,
      meta: s.meta,
    });
    if (s.endedAt) store.endSession(s.id, s.endedAt);
    sessions++;
  }

  let events = 0;
  for (const e of pack.events) {
    store.addEvent({
      sessionId: e.sessionId,
      ts: e.ts,
      type: e.type,
      tool: e.tool,
      summary: e.summary,
      payload: e.payload,
      redactions: e.redactions,
    });
    events++;
  }

  return { sessions, events };
}
